export const translations: Record<LangCode, Dict> = { fr, en, es, de, pt };

const SUPPORTED: LangCode[] = ['fr', 'en', 'es', 'de', 'pt'];

// Variable globale, lue par t() partout dans le code
let _currentLang: LangCode = (() => {
  try {
    const stored = localStorage.getItem('yt_lang');
    if (stored && SUPPORTED.includes(stored as LangCode)) return stored as LangCode;
  } catch {}
  const browser = ((typeof navigator !== 'undefined' ? navigator.language : null) || 'en')
    .slice(0, 2).toLowerCase();
  return (SUPPORTED.includes(browser as LangCode) ? browser : 'en') as LangCode;
})();

export function setGlobalLang(l: LangCode) {
  _currentLang = l;
  try { localStorage.setItem('yt_lang', l); } catch {}
}

export function getGlobalLang(): LangCode { return _currentLang; }

// La fonction t() utilise toujours la langue courante globale
export function t(key: string, lang?: LangCode): string {
  const useLang = lang || _currentLang;
  const dict = translations[useLang] || translations.fr;
  return dict[key] || translations.fr[key] || key;
}